package kr.co.ticketway.perform;

public class ReviewDTO {

	private String cmt_no;
	private String mno;
	private String mid;
	private String play_no;
	private String cmt_cnts;
	private String rating;
	private String cmt_date;
	
	
	
	
	
	
	
	public String getMno() {
		return mno;
	}
	public void setMno(String mno) {
		this.mno = mno;
	}
	public String getPlay_no() {
		return play_no;
	}
	public void setPlay_no(String play_no) {
		this.play_no = play_no;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getCmt_no() {
		return cmt_no;
	}
	public void setCmt_no(String cmt_no) {
		this.cmt_no = cmt_no;
	}
	
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	
	public String getCmt_cnts() {
		return cmt_cnts;
	}
	public void setCmt_cnts(String cmt_cnts) {
		this.cmt_cnts = cmt_cnts;
	}
	
	public String getCmt_date() {
		return cmt_date;
	}
	public void setCmt_date(String cmt_date) {
		this.cmt_date = cmt_date;
	}
	
	
	
}
